package com.green.java.ch03;

public class OperatorStudy2 {
    public static void main(String[] args) {
        int num = 10;
        int result = num % 2;
        System.out.printf("%d %% 2= %d\n", num , result); // %를 표현하고싶으면 %% 이렇게 사용하면된다.

        num = 9;
        result = num % 2;
        System.out.printf("%d %% 2 = %d\n",num,result);

    }
}
