package com.green.java.ch03;

public class OperatorStudy1 {
    public static void main(String[] args) {

        int num = 5;
        int result = 4 + num * 3;
        System.out.println(result);

        result = 10 / num;
        System.out.println(result);

        result = result - 1;
        System.out.println(result); //산술 연산자 + - * / 등등

        result = 13 % num;
        System.out.println(result);
        // %->mod(나머지라는 뜻) 짝수인지 홀수인지 체크할때 사용.


    }
}
